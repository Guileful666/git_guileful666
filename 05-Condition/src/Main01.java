
public class Main01 {

	public static void main(String[] args) {

		int age = 28;
		
		if (age == 28) {
			System.out.println("28살입니다.");
		}
		if (age != 19) {
			System.out.println("19살이 아닙니다.");
		}
		if (age > 19) {
			System.out.println("성인입니다.");
		}
		if (age <= 19) {
			System.out.println("미성년자입니다.");
		}


	}

}
